	String []arr=new String[6];
	ExamDetailsChck(String location){
		DataInputStream d = null;
                DataInputStream d2=null;
		try {
			d = new DataInputStream(new FileInputStream(location+"/ExamDetails.txt"));
                        d2 = new DataInputStream(new FileInputStream(location+"/qtype.txt"));
		} catch (FileNotFoundException e1) {
			// TODO Auto-generated catch block
			e1.printStackTrace();
		}
		
		String str;
		int f=0;
		try {
			while((str=d.readLine())!=null)
			{
				arr[f]=str;
				f++;
			}
                        arr[f]=d2.readLine();
		} catch (IOException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		
	}
	String[] getDetails(){
		return arr;
		
	}
