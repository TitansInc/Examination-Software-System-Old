    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
         
      
       if((!(jTextField1.getText().trim().equals("")))&&(!(jTextField2.getText().trim().equals("")))&&(!(jTextField3.getText().trim().equals("")))&&(!(jTextField5.getText().trim().equals("")))&&(!(jTextField6.getText().trim().equals("")))&&(!(jTextField7.getText().trim().equals("")))&&(!(jTextField8.getText().trim().equals("")))&&(!(jTextField13.getText().trim().equals("")))){
           if((Integer.parseInt(jTextField5.getText().trim())==jTextField3.getText().length())){
       
        SubjectsList.put(jTextField6.getText().trim(), jTextField8.getText().trim());
        Subjectfilesloc.put(jTextField6.getText().trim(), jTextField13.getText().trim());
        SubjectNoofques.put(jTextField6.getText().trim(), Integer.parseInt(jTextField5.getText().trim()));
        if(!Listmodel.contains(jTextField6.getText().trim())){
            Listmodel.addElement(jTextField6.getText().trim());
        }
        
        
         try {
            new CreateSubject(jTextField6.getText().trim(),jTextField7.getText().trim(),jTextField5.getText().trim(),jTextField1.getText().trim(),jTextField2.getText().trim(),jTextField8.getText().trim(),jTextField3.getText().trim());
        } catch (IOException ex) {
            Logger.getLogger(ServerSideComponent.class.getName()).log(Level.SEVERE, null, ex);
        }
         
         
        jTextArea1.setText(jTextArea1.getText()+jTextField6.getText().trim()+" Subject Created...\n");
        jTextField6.setText(null);
        jTextField7.setText(null);
        jTextField5.setText(null);
        jTextField3.setText(null);
        jTextField13.setText(null);
        jTextField1.setText(null);
        jTextField2.setText(null);
        jTextField8.setText(null);
       }else{
             
            jTextField3.setText("");
            WarningDialog wd=new WarningDialog(this,true,"Quest-type Field incorrect!");
            wd.setVisible(true);
           }
       }
       else{
           WarningDialog wd=new WarningDialog(this,true,"Please Fill All Entries!");
           wd.setVisible(true);
       }
    }//GEN-LAST:event_jButton3ActionPerformed

